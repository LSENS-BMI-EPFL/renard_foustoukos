def plot_transient_freq_vs_lmi_histogram(data_csv_path, save_path):
    """
    Boxplot: transient frequency (day 0) per LMI bin, one panel for R+ and R-.

    If transient_freq tracks LMI (positive slope), it would suggest that the
    participation–LMI relationship is confounded by spontaneous activity.

    Parameters
    ----------
    data_csv_path : str
        Path to the _data.csv saved by plot_participation_vs_transient_lmi.
    save_path : str
        Path to save SVG file.
    """
    if not os.path.exists(data_csv_path):
        print(f"  Warning: Data CSV not found: {data_csv_path}")
        return None

    merged = pd.read_csv(data_csv_path)
    merged = merged.dropna(subset=['lmi', 'transient_freq', 'reward_group'])

    lmi_bins = np.arange(-1.0, 1.2, 0.2)
    # Show full interval for each bin, e.g. "−1.0–−0.8"
    bin_labels = [f'{lmi_bins[k]:.1f}–{lmi_bins[k+1]:.1f}' for k in range(len(lmi_bins) - 1)]
    # Position of the LMI=0 boundary: between bin index 4 ([−0.2,0)) and 5 ([0,0.2))
    zero_boundary = 4.5

    group_colors = {'R+': reward_palette[1], 'R-': reward_palette[0]}

    fig, axes = plt.subplots(1, 2, figsize=(12, 5))

    for i, reward_group in enumerate(['R+', 'R-']):
        ax = axes[i]
        gdata = merged[merged['reward_group'] == reward_group].copy()

        if len(gdata) < 3:
            ax.text(0.5, 0.5, f'Insufficient data\n(n={len(gdata)})',
                    ha='center', va='center', transform=ax.transAxes, fontsize=12)
            ax.set_title(reward_group, fontweight='bold', fontsize=14)
            continue

        gdata['lmi_bin'] = pd.cut(
            gdata['lmi'], bins=lmi_bins, labels=bin_labels, include_lowest=True
        )

        sns.boxplot(
            data=gdata, x='lmi_bin', y='transient_freq',
            color=group_colors[reward_group], ax=ax,
            showfliers=False, linewidth=0.8
        )

        # Mark LMI = 0 boundary
        ax.axvline(zero_boundary, color='black', linestyle='--', linewidth=0.8, alpha=0.5)

        x = gdata['lmi'].values
        y = gdata['transient_freq'].values
        r, p = pearsonr(x, y)

        if p < 0.001:
            p_str = 'p < 0.001 ***'
        elif p < 0.01:
            p_str = f'p = {p:.3f} **'
        elif p < 0.05:
            p_str = f'p = {p:.3f} *'
        else:
            p_str = f'p = {p:.3f} ns'

        n_mice = gdata['mouse_id'].nunique()
        ax.text(0.05, 0.95,
                f'n = {len(x)} cells, {n_mice} mice\nPearson r = {r:.3f}\n{p_str}',
                transform=ax.transAxes, fontsize=10, va='top',
                bbox=dict(boxstyle='round', facecolor='white', alpha=0.9, edgecolor='gray'))

        ax.set_xlabel('LMI bin', fontweight='bold', fontsize=12)
        ax.set_ylabel('Transient frequency (events/min)' if i == 0 else '',
                      fontweight='bold', fontsize=12)
        ax.set_title(f'{reward_group}', fontweight='bold', fontsize=14)
        ax.tick_params(axis='x', rotation=45)
        ax.grid(True, alpha=0.3, axis='y')
        print(f"  {reward_group}: r={r:.3f}, p={p:.4f}, n={len(x)} cells, {n_mice} mice")

    fig.suptitle('Spontaneous Transient Frequency vs LMI (Day 0)',
                 fontsize=13, fontweight='bold')
    sns.despine()
    plt.tight_layout()

    if save_path:
        plt.savefig(save_path, format='svg', dpi=300, bbox_inches='tight')
        print(f"  Saved: {save_path}")

    return fig


