# ANALYSIS 3: TEMPORAL EVOLUTION WITH RESIDUAL PARTICIPATION RATE
# ============================================================================

def plot_temporal_evolution_residual(participation_csv, lmi_data_csv, save_path=None):
    """
    Temporal evolution of LMI+ vs LMI- participation rates across days,
    shown raw and after controlling for spontaneous transient frequency.

    Layout: 2 rows × 2 columns.
      Top row    : raw participation rate (mirrors temporal_evolution_per_mouse.svg)
      Bottom row : residual participation rate — linear contribution of transient_freq
                   removed (regression fitted on day-0 data per reward group, then
                   applied across all days using each cell's fixed transient_freq)
      Columns    : R+ (left), R- (right)

    Per-day paired t-tests (LMI+ vs LMI-) are printed and displayed for both
    raw and residual rows.

    Parameters
    ----------
    participation_csv : str
        Path to cell_participation_rates_per_day.csv.
    lmi_data_csv : str
        Path to participation_vs_transient_lmi_day0_data.csv
        (contains transient_freq, lmi, lmi_p, reward_group per cell).
    save_path : str or None
        Path to save SVG file.
    """
    for path, label in [(participation_csv, 'Participation CSV'),
                        (lmi_data_csv, 'LMI data CSV')]:
        if not os.path.exists(path):
            print(f"  Warning: {label} not found: {path}")
            return None

    # --- Load data ---
    participation_df = pd.read_csv(participation_csv)
    day0_df = pd.read_csv(lmi_data_csv).dropna(
        subset=['lmi', 'lmi_p', 'transient_freq', 'reward_group']
    )

    # Assign LMI category using the same thresholds as reactivation_lmi_prediction.py
    day0_df['lmi_category'] = 'neutral'
    day0_df.loc[day0_df['lmi_p'] >= 0.975, 'lmi_category'] = 'positive'
    day0_df.loc[day0_df['lmi_p'] <= 0.025, 'lmi_category'] = 'negative'
    day0_df = day0_df[day0_df['lmi_category'].isin(['positive', 'negative'])]

    # Merge all-days participation with cell info from day 0
    cell_info = day0_df[['mouse_id', 'roi', 'lmi_category', 'reward_group', 'transient_freq']]
    merged = pd.merge(participation_df, cell_info, on=['mouse_id', 'roi'], how='inner')

    # --- Residualise participation on transient_freq ---
    # Fit regression on day-0 data per reward group; apply across all days
    # (transient_freq is treated as a fixed per-cell covariate)
    merged['participation_resid'] = np.nan
    for rg in ['R+', 'R-']:
        day0_mask = (merged['day'] == 0) & (merged['reward_group'] == rg)
        d0 = merged[day0_mask]
        if len(d0) < 5:
            continue
        slope, intercept, _, _, _ = linregress(
            d0['transient_freq'].values, d0['participation_rate'].values
        )
        print(f"  {rg} residualisation: slope={slope:.4f}, intercept={intercept:.4f} "
              f"(n={len(d0)} cells on day 0)")
        grp_mask = merged['reward_group'] == rg
        merged.loc[grp_mask, 'participation_resid'] = (
            merged.loc[grp_mask, 'participation_rate']
            - (slope * merged.loc[grp_mask, 'transient_freq'] + intercept)
        )

    # --- Aggregate to per-mouse per-day means ---
    days_list = [-2, -1, 0, 1, 2]
    lmi_categories = ['positive', 'negative']
    reward_groups = ['R+', 'R-']
    colors = {'positive': '#d62728', 'negative': '#1f77b4'}

    def _aggregate(df, metric):
        records = []
        for rg in reward_groups:
            for lmi_cat in lmi_categories:
                mask = (df['reward_group'] == rg) & (df['lmi_category'] == lmi_cat)
                cells = df[mask][['mouse_id', 'roi']].drop_duplicates()
                for mouse_id in cells['mouse_id'].unique():
                    mouse_rois = cells[cells['mouse_id'] == mouse_id]['roi'].values
                    for day in days_list:
                        day_mask = (
                            (df['mouse_id'] == mouse_id) &
                            (df['roi'].isin(mouse_rois)) &
                            (df['day'] == day)
                        )
                        vals = df[day_mask][metric].dropna().values
                        records.append({
                            'reward_group': rg,
                            'lmi_category': lmi_cat,
                            'mouse_id': mouse_id,
                            'day': day,
                            'value': np.mean(vals) if len(vals) > 0 else np.nan
                        })
        return pd.DataFrame(records)

    df_raw = _aggregate(merged, 'participation_rate')
    df_resid = _aggregate(merged, 'participation_resid')

    # --- Per-day paired t-tests (LMI+ vs LMI-) ---
    def _posthoc(df_agg, rg, label):
        df_rg = df_agg[df_agg['reward_group'] == rg]
        df_pos = df_rg[df_rg['lmi_category'] == 'positive']
        df_neg = df_rg[df_rg['lmi_category'] == 'negative']
        results = {d: {'p': np.nan, 'significant': False} for d in days_list}

        print(f"\n  {rg} [{label}] paired t-tests (LMI+ vs LMI-):")
        for day in days_list:
            pos_d = df_pos[df_pos['day'] == day]
            neg_d = df_neg[df_neg['day'] == day]
            mice_both = sorted(set(pos_d['mouse_id']) & set(neg_d['mouse_id']))
            if len(mice_both) >= 3:
                pos_v = [pos_d[pos_d['mouse_id'] == m]['value'].values[0]
                         for m in mice_both]
                neg_v = [neg_d[neg_d['mouse_id'] == m]['value'].values[0]
                         for m in mice_both]
                try:
                    _, p = ttest_rel(pos_v, neg_v)
                    sig = ('***' if p < 0.001 else '**' if p < 0.01
                           else '*' if p < 0.05 else 'ns')
                    results[day] = {'p': p, 'significant': p < 0.05}
                    print(f"    Day {day:+2d}: n={len(mice_both)} mice, p={p:.4f} {sig}")
                except Exception:
                    print(f"    Day {day:+2d}: test failed")
            else:
                print(f"    Day {day:+2d}: insufficient paired data (n={len(mice_both)})")
        return results

    # --- Plot ---
    row_configs = [
        (df_raw,   'Participation rate',                              'Raw'),
        (df_resid, 'Residual participation rate\n(ctrl transient freq)', 'Residual'),
    ]

    fig, axes = plt.subplots(2, 2, figsize=(14, 10))

    for row, (df_agg, ylabel, row_label) in enumerate(row_configs):
        for col, rg in enumerate(reward_groups):
            ax = axes[row, col]
            df_rg = df_agg[df_agg['reward_group'] == rg].dropna(subset=['value'])
            posthoc = _posthoc(df_agg, rg, row_label)

            # Seaborn barplot: mean ± 95% CI, no end caps on error bars
            sns.barplot(
                data=df_rg, x='day', y='value', hue='lmi_category',
                hue_order=lmi_categories, palette=colors, order=days_list,
                estimator=np.mean, errorbar=('ci', 95), capsize=0,
                err_kws={'linewidth': 1.5}, ax=ax
            )
            for patch in ax.patches:
                patch.set_alpha(0.7)
                patch.set_edgecolor('black')
                patch.set_linewidth(0.8)

            # Individual mouse trajectories, using bar centers from ax.containers
            for j, lmi_cat in enumerate(lmi_categories):
                if j >= len(ax.containers):
                    continue
                cat_data = df_rg[df_rg['lmi_category'] == lmi_cat]
                x_centers = {days_list[k]: bar.get_x() + bar.get_width() / 2
                             for k, bar in enumerate(ax.containers[j])}
                for mouse_id in cat_data['mouse_id'].unique():
                    mdata = cat_data[cat_data['mouse_id'] == mouse_id].sort_values('day')
                    mx = [x_centers[d] for d in mdata['day'] if d in x_centers]
                    my = mdata['value'].values
                    ax.plot(mx, my, '-', color=colors[lmi_cat],
                            linewidth=0.8, alpha=0.4, zorder=5)

            # Fix y-axis for raw row to match original figure
            if row == 0:
                ax.set_ylim(0, 0.6)

            # Significance markers
            y_sig = ax.get_ylim()[1] * 0.92
            for k, day in enumerate(days_list):
                p_val = posthoc[day]['p']
                if not np.isnan(p_val) and p_val < 0.05:
                    sig = '***' if p_val < 0.001 else '**' if p_val < 0.01 else '*'
                    x0 = ax.containers[0][k].get_x() + ax.containers[0][k].get_width() / 2
                    x1 = ax.containers[1][k].get_x() + ax.containers[1][k].get_width() / 2
                    ax.text((x0 + x1) / 2, y_sig, sig,
                            ha='center', va='bottom', fontsize=12, fontweight='bold')

            ax.set_xlabel('Day', fontweight='bold', fontsize=12)
            ax.set_ylabel(ylabel, fontweight='bold', fontsize=11)
            ax.set_title(f'{rg} Mice — {row_label}', fontweight='bold', fontsize=13)
            handles, labels = ax.get_legend_handles_labels()
            ax.legend(handles, [f'{l.capitalize()} LMI' for l in labels],
                      loc='best', fontsize=10)
            ax.grid(True, alpha=0.3, axis='y')
            sns.despine(ax=ax)

    fig.suptitle(
        'Temporal Evolution: LMI+ vs LMI- Participation Rate\n(Raw and Residual)',
        fontsize=13, fontweight='bold'
    )
    plt.tight_layout()

    if save_path:
        plt.savefig(save_path, format='svg', dpi=300, bbox_inches='tight')
        print(f"\n  Saved: {save_path}")

    return fig


